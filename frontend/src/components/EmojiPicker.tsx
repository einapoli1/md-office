const RECENT_KEY = 'md-office-recent-emojis';
const MAX_RECENT = 24;

const EmojiPicker: React.FC<EmojiPickerProps> = ({ onSelect, onClose }) => {
  const [search, setSearch] = useState('');
  const [activeCategory, setActiveCategory] = useState('Smileys');
  const [recentEmojis, setRecentEmojis] = useState<string[]>([]);
  const panelRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    try {
      const stored = localStorage.getItem(RECENT_KEY);
      if (stored) setRecentEmojis(JSON.parse(stored));
    } catch {}
  }, []);

  useEffect(() => {
    const handleClickOutside = (e: MouseEvent) => {
      if (panelRef.current && !panelRef.current.contains(e.target as Node)) {
        onClose();
      }
    };
    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [onClose]);

  const handleSelect = (emoji: string) => {
    const updated = [emoji, ...recentEmojis.filter(e => e !== emoji)].slice(0, MAX_RECENT);
    setRecentEmojis(updated);
    localStorage.setItem(RECENT_KEY, JSON.stringify(updated));
    onSelect(emoji);
  };

  const allEmojis = search
    ? Object.values(EMOJI_CATEGORIES).flat().filter(() => true) // Can't really search emoji by text without a mapping, show all and let user scroll
    : null;

  const displayEmojis = allEmojis || EMOJI_CATEGORIES[activeCategory] || [];

  return (
    <div className="emoji-picker-panel" ref={panelRef}>
      <div className="emoji-picker-header">
        <input
          type="text"
          className="emoji-picker-search"
          placeholder="Search emojis..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          autoFocus
        />
        <button className="emoji-picker-close" onClick={onClose}><X size={14} /></button>
      </div>

      {!search && (
        <div className="emoji-picker-categories">
          {recentEmojis.length > 0 && (
            <button
              className={`emoji-category-btn ${activeCategory === 'Recent' ? 'active' : ''}`}
              onClick={() => setActiveCategory('Recent')}
              title="Recent"
            >
              <Clock size={14} />
            </button>
          )}
          {Object.keys(EMOJI_CATEGORIES).map(cat => (
            <button
              key={cat}
              className={`emoji-category-btn ${activeCategory === cat ? 'active' : ''}`}
              onClick={() => setActiveCategory(cat)}
              title={cat}
            >
              {cat[0]}
            </button>
          ))}
        </div>
      )}

      <div className="emoji-picker-grid">
        {activeCategory === 'Recent' && !search
          ? recentEmojis.map((emoji, i) => (
              <button key={i} className="emoji-picker-item" onClick={() => handleSelect(emoji)}>{emoji}</button>
            ))
          : displayEmojis.map((emoji, i) => (
              <button key={i} className="emoji-picker-item" onClick={() => handleSelect(emoji)}>{emoji}</button>
            ))
        }
      </div>
    </div>
  );
};

export default EmojiPicker;
